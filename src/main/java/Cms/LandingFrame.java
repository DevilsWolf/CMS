    public boolean verifyUser() throws IOException {
        try {
            File f = new File("UserData.txt");
            if (!f.exists()) {
                f.createNewFile();
            }
            Scanner scanFile = new Scanner(f);
            while (scanFile.hasNext()) {
                String line = scanFile.nextLine();
                String[] values = line.split(",");
                if (username.getText().equals(values[1]) && password.getText().equals(values[3])) {
                    JOptionPane.showMessageDialog(this, "Welcome " + values[1] + " " + values[2] + " it is great to see you!", "A Successful Login", 1);
                    return true;
                }
            }
        } catch (FileNotFoundException ex) {
           // Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        }
        return false;

    }

